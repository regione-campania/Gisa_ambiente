CREATE TABLE sync_client (
  client_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  "type" VARCHAR(100),
  "version" VARCHAR(50),
  entered TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  enteredby INT NOT NULL,
  modified TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  modifiedby INT NOT NULL,
  anchor TIMESTAMP DEFAULT NULL,
  enabled CHAR(1) DEFAULT '0',
  code VARCHAR(255),
  user_id INT REFERENCES "access"(user_id),
  package_file_id INT REFERENCES project_files(item_id)
);

CREATE TABLE sync_system (
  system_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  application_name VARCHAR(255),
  enabled CHAR(1) DEFAULT '1'
);

CREATE TABLE sync_table (
  table_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  system_id INT NOT NULL REFERENCES sync_system(system_id),
  element_name VARCHAR(255),
  mapped_class_name VARCHAR(255),
  entered TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  modified TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  create_statement CLOB,
  order_id INT DEFAULT -1,
  sync_item CHAR(1) DEFAULT '0',
  object_key VARCHAR(50)
);

CREATE TABLE sync_map (
  client_id INT NOT NULL REFERENCES sync_client(client_id),
  table_id INT NOT NULL REFERENCES sync_table(table_id),
  record_id INT NOT NULL,
  cuid INT NOT NULL,
  complete CHAR(1) DEFAULT '0',
  status_date TIMESTAMP
);

CREATE UNIQUE INDEX idx_sync_map ON sync_map (client_id, table_id, record_id);

CREATE TABLE sync_conflict_log (
  client_id INT NOT NULL REFERENCES sync_client(client_id),
  table_id INT NOT NULL REFERENCES sync_table(table_id),
  record_id INT NOT NULL,
  status_date TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE sync_log (
  log_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  system_id INT NOT NULL REFERENCES sync_system(system_id),
  client_id INT NOT NULL REFERENCES sync_client(client_id),
  ip VARCHAR(30),
  entered TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE sync_transaction_log (
  transaction_id INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
  log_id INT NOT NULL REFERENCES sync_log(log_id),
  reference_id VARCHAR(50),
  element_name VARCHAR(255),
  "action" VARCHAR(20),
  link_item_id INT,
  status_code INT,
  record_count INT,
  "message" CLOB
);

CREATE TABLE process_log (
  process_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  system_id INT NOT NULL REFERENCES sync_system(system_id),
  client_id INT NOT NULL REFERENCES sync_client(client_id),
  entered TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  process_name VARCHAR(255),
  process_version VARCHAR(20),
  status INT,
  "message" CLOB
);

CREATE TABLE sync_package (
  package_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  client_id INT NOT NULL REFERENCES sync_client(client_id),
  "type" INT NOT NULL,
  "size" INT DEFAULT 0,
  status_id INT NOT NULL,
  recipient INT NOT NULL,
  status_date TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  last_anchor TIMESTAMP DEFAULT NULL,
  next_anchor TIMESTAMP NOT NULL,
  package_file_id INT REFERENCES project_files(item_id),
  entered TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE sync_package_data (
  data_id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  package_id INT NOT NULL REFERENCES sync_package(package_id),
  table_id INT NOT NULL REFERENCES sync_table(table_id),
  "action" INT NOT NULL,
  identity_start INT NOT NULL,
  "offset" INT,
  "items" INT,
  last_anchor TIMESTAMP DEFAULT NULL,
  next_anchor TIMESTAMP NOT NULL,
  entered TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

