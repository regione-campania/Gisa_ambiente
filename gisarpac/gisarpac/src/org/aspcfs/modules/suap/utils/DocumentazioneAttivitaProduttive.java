package org.aspcfs.modules.suap.utils;

public class DocumentazioneAttivitaProduttive {
	
	private String allegatoNome ;
	private String allegatoDescrizione ;
	private String descrizioneMacroarea;
	private String descrizioneAggregazione ;
	private String descrizioneAttivita;
	public String getAllegatoNome() {
		return allegatoNome;
	}
	public void setAllegatoNome(String allegatoNome) {
		this.allegatoNome = allegatoNome;
	}
	public String getAllegatoDescrizione() {
		return allegatoDescrizione;
	}
	public void setAllegatoDescrizione(String allegatoDescrizione) {
		this.allegatoDescrizione = allegatoDescrizione;
	}
	public String getDescrizioneMacroarea() {
		return descrizioneMacroarea;
	}
	public void setDescrizioneMacroarea(String descrizioneMacroarea) {
		this.descrizioneMacroarea = descrizioneMacroarea;
	}
	public String getDescrizioneAggregazione() {
		return descrizioneAggregazione;
	}
	public void setDescrizioneAggregazione(String descrizioneAggregazione) {
		this.descrizioneAggregazione = descrizioneAggregazione;
	}
	public String getDescrizioneAttivita() {
		return descrizioneAttivita;
	}
	public void setDescrizioneAttivita(String descrizioneAttivita) {
		this.descrizioneAttivita = descrizioneAttivita;
	}
	
	
	

}
